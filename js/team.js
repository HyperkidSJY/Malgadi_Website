function display(members_array, yr, no){
  const obj = document.querySelector(`#te-main-${yr}-${no}`);
  for(let i = 0; i < members_array.length; i++){
    var div1 = document.createElement('div');
    var div2 = document.createElement('div');
    var div3 = document.createElement('div');
    var image = document.createElement('img');
    image.src = members_array[i].photo ;
    image.height = "230";
    image.width = "230";
    image.classList.add("img-fluid");
    //image.classList.add("sepia");
    div3.appendChild(image);
    div3.classList.add("rounded-circle");
    div3.classList.add("overflow-hidden");
    div3.classList.add("m-4");
    div2.appendChild(div3);
    var info1 = document.createElement('h5');
    var detail = document.createTextNode(members_array[i].name);
    info1.style.textAlign = "center";
    //info1.style.bottommargin = "0.4px";
    info1.appendChild(detail);
    div2.appendChild(info1);
    var info2 = document.createElement('h6');
    var details = document.createTextNode(members_array[i].team);
    info2.style.textAlign = "center";
    //p2.style.topmargin = "0.1px";
    info2.appendChild(details);
    div2.appendChild(info2);
    div2.classList.add("team-item");
    div2.classList.add("border");
    div2.classList.add("text-center");
    // div2.classList.add("rounded");
    div2.classList.add("overflow-hidden");
    div1.appendChild(div2);
    div1.classList.add("col-lg-3");
    div1.classList.add("col-md-4");
    div1.classList.add("col-sm-6");
    div1.classList.add("col-12");
    div1.classList.add("mx");
    div1.classList.add("wow");
    div1.classList.add("fadeInUp");
    div1.style.visibility = "visible";
    div1.style.animationDelay = "0.1s";
    div1.style.animationName = "fadeInUp";
    //div1.style.backgroundColor = "linen";
    //div1.style.border = "solid";
    //div1.data-wow-delay = "0.1s";
    obj.appendChild(div1);
  }
  // global_k++;
}

