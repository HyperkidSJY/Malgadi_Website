var cnt = 1;

function display_navbar_mobile(){
  var obj = document.querySelector(".te-nav");
  if(cnt % 2 == 1){
    obj.style.display = "block";
  }
  else{
    obj.style.display = "none";
  }
  cnt++;
}

function show(year){
  var year_arr = [2015, 2016, 2017, 2018, 2019, 2020, 2021];
  for(var i = 0; i < year_arr.length; i++){
    var obj = document.querySelector(`#te-yr-${year_arr[i]}`);
    if(year_arr[i] == year){
      obj.style.display = "block";
    }
    else{
      obj.style.display = "none";
    }
  }
}